document.addEventListener("DOMContentLoaded", () => {
    const langEnButton = document.getElementById("lang-en");
    const langUaButton = document.getElementById("lang-ua");
    let currentLang = localStorage.getItem("language") || "en"; // Default to English

    const setLanguage = (lang) => {
        currentLang = lang;
        localStorage.setItem("language", lang);
        document.documentElement.lang = lang;

        document.querySelectorAll("[data-en], [data-ua]").forEach(el => {
            const enText = el.getAttribute("data-en");
            const uaText = el.getAttribute("data-ua");

            if (lang === "ua" && uaText) {
                if (el.tagName === "IMG" || el.tagName === "INPUT") {
                    if (el.hasAttribute("alt")) el.alt = translations[lang][el.getAttribute("data-ua-alt")] || uaText;
                    if (el.hasAttribute("title")) el.title = translations[lang][el.getAttribute("data-ua-title")] || uaText; // For tooltips if any
                } else {
                    el.innerHTML = translations[lang][uaText] || uaText;
                }
            } else if (enText) {
                 if (el.tagName === "IMG" || el.tagName === "INPUT") {
                    if (el.hasAttribute("alt")) el.alt = translations[lang][el.getAttribute("data-en-alt")] || enText;
                    if (el.hasAttribute("title")) el.title = translations[lang][el.getAttribute("data-en-title")] || enText;
                } else {
                    el.innerHTML = translations[lang][enText] || enText;
                }
            }
        });
        // Special case for the main title tag
        const titleTag = document.querySelector("title");
        if (titleTag) {
            const enTitle = titleTag.getAttribute("data-en");
            const uaTitle = titleTag.getAttribute("data-ua");
            if (lang === "ua" && uaTitle) {
                document.title = translations[lang][uaTitle] || uaTitle;
            } else if (enTitle) {
                document.title = translations[lang][enTitle] || enTitle;
            }
        }

        updateButtonStyles();
    };

    const updateButtonStyles = () => {
        if (currentLang === "ua") {
            langUaButton.classList.add("active");
            langEnButton.classList.remove("active");
        } else {
            langEnButton.classList.add("active");
            langUaButton.classList.remove("active");
        }
    };

    langEnButton.addEventListener("click", () => setLanguage("en"));
    langUaButton.addEventListener("click", () => setLanguage("ua"));

    // Set initial language on load
    setLanguage(currentLang);
});


